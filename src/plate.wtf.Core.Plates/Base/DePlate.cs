        public Plate Parse(string plate)
        {
            Plate plateReturn = new Plate {};

            if(Regex.IsMatch(plate, Standard1956Regex))
            {
                plateReturn.Info = ParseStandard1956Plate(plate);
                plateReturn.Valid = true;
            }
            else if(Regex.IsMatch(plate, Standard1956ElectricRegex))
            {
                plateReturn.Info = ParseStandard1956Plate(plate, true);
                plateReturn.Valid = true;
            }
            else if(Regex.IsMatch(plate, Diplomatic1956Regex))
            {
                Enums.PlateFormat plateFormat = Enums.PlateFormat.De_Diplomatic1956;

                plateReturn.Info = new PlateInfo
                {
                    Format = EnumConstants.GetPlateFormatConstant(plateFormat),
                    FormatEnum = plateFormat
                };
                plateReturn.Valid = true;
            }
            else
            {
                plateReturn.Valid = false;
            }

            plateReturn.Country = new Country
            {
                Code = "de",
                Flag = "üá©üá™"
            };

            plateReturn.Serial = plate;

            return plateReturn;
        }

        private static PlateInfo ParseStandard1956Plate(string plate, bool electricSpecial = false)
        {
            string regionOrSpecial = null;
            string regionString = null;
            string specialString = null;

            if(!electricSpecial)
            {
                Regex regex = new Regex(Standard1956Regex);
                Match match = regex.Match(plate);

                regionOrSpecial = match.Groups[2].Value;
            }
            else
            {
                Regex regex = new Regex(Standard1956Regex);
                Match match = regex.Match(plate);

                regionOrSpecial = match.Groups[2].Value;
            }

            Enums.PlateFormat plateFormat = Enums.PlateFormat.De_Standard1956;

            PlateInfo returnModel = new PlateInfo
            {
                Format = EnumConstants.GetPlateFormatConstant(plateFormat),
                FormatEnum = plateFormat,
                Region = null,
                Special = null
            };

            if(Post1956SpecialCodes.ContainsKey(regionOrSpecial))
            {
                Post1956SpecialCodes.TryGetValue(regionOrSpecial, out specialString);
                returnModel.Special = specialString;
            }
            else if(Post1956LocationCodes.ContainsKey(regionOrSpecial))
            {
                Post1956LocationCodes.TryGetValue(regionOrSpecial, out regionString);
                returnModel.Region = regionString;
            }

            if(electricSpecial) {
                returnModel.Special = "Electric Vehicle";
            }

            return returnModel;
        }

